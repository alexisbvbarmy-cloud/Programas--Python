# Demostrar el uso de los Operadores de Asignación 

print("-"*40)
print("Operadores de Asignación en Python")
print("-"*40)

x= 10
print(f"El valor inicial de x : {x}")

# Aplicar diferentes operadores de asignación 
x += 5
print(f"x += 5 --> x= {x}")
x -= 3
print(f"x -= 3 --> x= {x}")
x *= 2
print(f"x *= 2 --> x= {x}")
x %= 5
print(f"x %= 5 --> x= {x}")
x **= 2
print(f"x **= 2 --> x= {x}")
x //= 2
print(f"x //= 2 --> x= {x}")

